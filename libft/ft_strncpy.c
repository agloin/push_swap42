#include "libft.h"

char	*ft_strncpy(char *dest, const char *source, size_t n)
{
	size_t	i;

	i = 0;
	while (source[i] != '\0' && i != n)
	{
		dest[i] = source[i];
		i++;
	}
	while (i < n)
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}
